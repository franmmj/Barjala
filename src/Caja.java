import java.util.*;
public class Caja {
	float cantidadInicio;
	float cantidadFin;
	Date fechaInicio;
	Date fechaFin;

public Caja()
	{
	
	}

public float getCantidadInicio() {
	return cantidadInicio;
}

public void setCantidadInicio(float cantidadInicio) {
	this.cantidadInicio = cantidadInicio;
}

public float getCantidadFin() {
	return cantidadFin;
}

public void setCantidadFin(float cantidadFin) {
	this.cantidadFin = cantidadFin;
}

public Date getFechaInicio() {
	return fechaInicio;
}

public void setFechaInicio(Date fechaInicio) {
	this.fechaInicio = fechaInicio;
}

public Date getFechaFin() {
	return fechaFin;
}

public void setFechaFin(Date fechaFin) {
	this.fechaFin = fechaFin;
}

}
