

public class Terminal {
	int id;
	String ubicacion;
	Caja caja;
	
public Terminal()
	{
	
	}

public int getId() {
	return id;
}

public void setId(int id) {
	this.id = id;
}

public String getUbicacion() {
	return ubicacion;
}

public void setUbicacion(String ubicacion) {
	this.ubicacion = ubicacion;
}

public Caja getCaja() {
	return caja;
}

public void setCaja(Caja caja) {
	this.caja = caja;
}


}
